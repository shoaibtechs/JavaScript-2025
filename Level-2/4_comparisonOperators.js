// Equal to (==)
console.log(5 == '5'); // true - only checks value, not type

// Not equal to (!=)
console.log(5 != '5'); // false - only checks value, not type

// Strict equal to (===)
console.log(5 === '5'); // false - checks both value and type

// Strict not equal to (!==)
console.log(5 !== '5'); // true - checks both value and type

// Greater than (>)
console.log(5 > 3); // true

// Less than (<)
console.log(5 < 3); // false

// Greater than or equal to (>=)
console.log(5 >= 5); // true

// Less than or equal to (<=)
console.log(5 <= 5); // true








