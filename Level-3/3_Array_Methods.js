let cars = ["Alto", "Mehran", "Suzuki "];
console.log("Orginal Cars1: ", cars)



// --------------------1. Push -----------------

// It will add the element at the last index of an array 

cars.push("BMW");
console.log(cars);



// --------------------2. PoP -----------------

// It will deletes the last element 


cars.pop();   
console.log(cars);





